let num2 = 2.5

let sum = 0; difference = 0; product = 0; quotient = 0 //initialization

sum = num1 + num2
difference = num2 - num1
product = num1 * num2
quotient = num2 % num1

console.log(sum)
console.log(difference)
console.log(product)
console.log(quotient)
