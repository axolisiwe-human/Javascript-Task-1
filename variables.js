//Variable declaration

let name = "Human"
let age = 24
let isStudent = false

console.log(name)
console.log(age)
console.log(isStudent)

//Variable reassignment

let favoriteColor = "Green"
console.log(favoriteColor)

favoriteColor = "Black" //reassigning to a different color
console.log(favoriteColor)

//Arithmetic Operations

